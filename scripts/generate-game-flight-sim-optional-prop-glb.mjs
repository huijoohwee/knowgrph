import { createHash } from 'node:crypto'
import { readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import process from 'node:process'
import { fileURLToPath } from 'node:url'

const repositoryRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const fallbackDirectory = path.join(
  repositoryRoot,
  'canvas/src/features/game-flight-sim/assetSpec/fallbacks',
)
const binaryPath = path.join(fallbackDirectory, 'optional-beacon.glb')
const generatedSourcePath = path.join(fallbackDirectory, 'optionalBeaconGlb.generated.ts')
const checkOnly = process.argv.includes('--check')

function padBytes(bytes, alignment, fill) {
  const remainder = bytes.byteLength % alignment
  if (remainder === 0) return bytes
  const output = Buffer.alloc(bytes.byteLength + alignment - remainder, fill)
  bytes.copy(output)
  return output
}

function createTriangleBuffer() {
  const positions = [
    -0.5, 0, 0,
    0.5, 0, 0,
    0, 1, 0,
  ]
  const indices = [0, 1, 2]
  const rawByteLength = positions.length * Float32Array.BYTES_PER_ELEMENT
    + indices.length * Uint16Array.BYTES_PER_ELEMENT
  const buffer = Buffer.alloc(rawByteLength)
  positions.forEach((value, index) => buffer.writeFloatLE(
    value,
    index * Float32Array.BYTES_PER_ELEMENT,
  ))
  const indexOffset = positions.length * Float32Array.BYTES_PER_ELEMENT
  indices.forEach((value, index) => buffer.writeUInt16LE(
    value,
    indexOffset + index * Uint16Array.BYTES_PER_ELEMENT,
  ))
  return { bytes: padBytes(buffer, 4, 0), declaredByteLength: rawByteLength }
}

function createGlb() {
  const binary = createTriangleBuffer()
  const document = {
    asset: {
      version: '2.0',
      generator: 'knowgrph-offline-flight-sim-fallback/v1',
      copyright: 'Knowgrph contributors; CC0-1.0',
    },
    scene: 0,
    scenes: [{ nodes: [0] }],
    nodes: [{ mesh: 0, name: 'FlightSimOptionalBeacon' }],
    meshes: [{
      primitives: [{
        attributes: { POSITION: 0 },
        indices: 1,
        mode: 4,
      }],
    }],
    buffers: [{ byteLength: binary.declaredByteLength }],
    bufferViews: [
      { buffer: 0, byteOffset: 0, byteLength: 36, target: 34962 },
      { buffer: 0, byteOffset: 36, byteLength: 6, target: 34963 },
    ],
    accessors: [
      {
        bufferView: 0,
        componentType: 5126,
        count: 3,
        type: 'VEC3',
        min: [-0.5, 0, 0],
        max: [0.5, 1, 0],
      },
      {
        bufferView: 1,
        componentType: 5123,
        count: 3,
        type: 'SCALAR',
        min: [0],
        max: [2],
      },
    ],
  }
  const json = padBytes(Buffer.from(JSON.stringify(document), 'utf8'), 4, 0x20)
  const totalByteLength = 12 + 8 + json.byteLength + 8 + binary.bytes.byteLength
  const glb = Buffer.alloc(totalByteLength)
  glb.writeUInt32LE(0x46546c67, 0)
  glb.writeUInt32LE(2, 4)
  glb.writeUInt32LE(totalByteLength, 8)
  glb.writeUInt32LE(json.byteLength, 12)
  glb.writeUInt32LE(0x4e4f534a, 16)
  json.copy(glb, 20)
  const binaryHeaderOffset = 20 + json.byteLength
  glb.writeUInt32LE(binary.bytes.byteLength, binaryHeaderOffset)
  glb.writeUInt32LE(0x004e4942, binaryHeaderOffset + 4)
  binary.bytes.copy(glb, binaryHeaderOffset + 8)
  return glb
}

function generatedSource(bytes) {
  const sha256 = createHash('sha256').update(bytes).digest('hex')
  return `// Generated by scripts/generate-game-flight-sim-optional-prop-glb.mjs.
// Source-authored offline geometry; CC0-1.0. Do not edit by hand.
export const FLIGHT_SIM_OPTIONAL_BEACON_GLB_SOURCE_PATH =
  'canvas/src/features/game-flight-sim/assetSpec/fallbacks/optional-beacon.glb' as const
export const FLIGHT_SIM_OPTIONAL_BEACON_GLB_SHA256 = '${sha256}' as const
export const FLIGHT_SIM_OPTIONAL_BEACON_GLB_HEX = '${bytes.toString('hex')}' as const
`
}

async function readOrNull(filePath) {
  try {
    return await readFile(filePath)
  } catch (error) {
    if (error?.code === 'ENOENT') return null
    throw error
  }
}

async function requireExact(filePath, expected) {
  const current = await readOrNull(filePath)
  if (!current || !current.equals(expected)) {
    throw new Error(`${path.relative(repositoryRoot, filePath)} is not the deterministic generated output`)
  }
}

const glb = createGlb()
const source = Buffer.from(generatedSource(glb), 'utf8')
if (checkOnly) {
  await requireExact(binaryPath, glb)
  await requireExact(generatedSourcePath, source)
  console.log(`OK deterministic Flight Sim optional prop GLB (${glb.byteLength} bytes)`)
} else {
  await writeFile(binaryPath, glb)
  await writeFile(generatedSourcePath, source)
  console.log(`Wrote deterministic Flight Sim optional prop GLB (${glb.byteLength} bytes)`)
}
