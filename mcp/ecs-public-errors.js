function publicCode(value, allowlist, fallback) {
  return typeof value === "string" && allowlist.has(value) ? value : fallback;
}

export const publicKgcErrorCode = (value, fallback) => publicCode(value, KGC_ERROR_CODES, fallback);
export const publicSessionErrorCode = (value, fallback) => publicCode(value, SESSION_ERROR_CODES, fallback);
export const publicTickErrorCode = (value, fallback) => publicCode(value, TICK_ERROR_CODES, fallback);
export const publicDeferredReason = (value) => publicCode(value, DEFERRED_REASONS, "executor_unavailable");
